namespace AutoEverything.Core
{
    /// <summary>
    /// 殖民者战斗价值档次（用于 ITab 显示、殖民者栏排序、高价值标记判定与 DEBUG 显示）。
    /// 设计：把"战斗价值"离散化为 8 档，便于玩家直观判断优先级。
    ///   X：无法从事暴力活动（如医疗特质 DisableViolent）
    ///   D：无火无战斗特质（基础农民）
    ///   C：无火但有战斗特质（如 Tough 农民）
    ///   B：单火/单 Major（任一射击或近战有兴趣，但非双 Major）
    ///   A：双火（射击+近战均为 Major 兴趣）
    ///   S：满足任一特殊组合（坚韧+格斗有火 / 乱开枪+射击单火 / 工作狂+神经质+1双火 / 特殊天赋 / 沉鱼落雁+社交双火）
    ///   SS：强化组合（乱开枪+射击双火 / 坚韧+格斗双火 / 工作狂+神经质+2双火）
    ///   SSS：顶级组合（乱开枪+坚韧+射击双火 / 坚韧+格斗双火+敏捷或格斗者 / 工作狂+神经质+3双火）
    /// 有负面特质（脑子慢/纵火狂/脆弱/工作懒惰怠惰）降一档。
    /// </summary>
    public enum CombatTier : byte
    {
        X = 0,
        D = 1,
        C = 2,
        B = 3,
        A = 4,
        S = 5,
        SS = 6,
        SSS = 7
    }
}
